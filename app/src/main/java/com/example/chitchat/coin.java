package com.example.chitchat;

public class coin {


    public int playerCoinCount = 100000;

    public void addCoin(int r){

    if(r == 0){
        playerCoinCount = playerCoinCount;
    }
    else{
        playerCoinCount = playerCoinCount + r;


    }

    }

    public void subtractCoin(int r){
        if(r == 0){
            playerCoinCount = playerCoinCount;
        }
        else{
            playerCoinCount = playerCoinCount - r;


        }

    }






}
